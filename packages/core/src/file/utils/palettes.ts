/** Generate 256 color strings for the purpose of grayscale palette. */
const generateGrayscalePalette = () => {

    const result: string[] = [];

    for (let i = 0; i <= 255; i++) {

        result.push(`rgb(${i},${i},${i})`);

    }

    return result;

};

